export const analyzeName = (xing, ming) => {
  const fullName = xing + ming
  const chars = fullName.split('')
  
  const tiange = chars.slice(0, 1).reduce((sum, char) => sum + (getBihua(char) || 0), 0) % 10 || 10
  const ren = chars.slice(1, 2).reduce((sum, char) => sum + (getBihua(char) || 0), 0) % 10 || 10
  const dige = chars.slice(2, 3).reduce((sum, char) => sum + (getBihua(char) || 0), 0) % 10 || 10
  const zongge = chars.reduce((sum, char) => sum + (getBihua(char) || 0), 0) % 10 || 10
  
  const tianInfo = tianTiange[tiange] || { score: '未知', desc: '无法解析', yinyang: '未知' }
  const renInfo = renDigang[ren] || { wuxing: '未知', desc: '无法解析', yinyang: '未知' }
  const diInfo = dizhige[dige] || { wuxing: '未知', desc: '无法解析', yinyang: '未知' }
  const zongInfo = zongge[zongge] || { score: '未知', desc: '无法解析', yinyang: '未知' }
  
  const wuxingCount = {
    '金': 0, '木': 0, '水': 0, '火': 0, '土': 0
  }
  
  chars.forEach(char => {
    const w = getCharWuxing(char)
    if (wuxingCount[w] !== undefined) {
      wuxingCount[w]++
    }
  })
  
  const dominantWuxing = Object.entries(wuxingCount).reduce((a, b) => a[1] > b[1] ? a : b)[0]
  
  let totalScore = 60
  if (tianInfo.score === '大吉') totalScore += 10
  if (renInfo.wuxing === dominantWuxing) totalScore += 5
  if (diInfo.wuxing === dominantWuxing) totalScore += 5
  if (zongInfo.score === '大吉') totalScore += 10
  
  const yinyangBalance = [tianInfo.yinyang, renInfo.yinyang, diInfo.yinyang].filter(y => y && y.includes('阳')).length
  if (yinyangBalance >= 2) totalScore += 5
  
  totalScore = Math.min(100, totalScore)
  
  return {
    xing,
    ming,
    fullName,
    tiange: { number: tiange, ...tianInfo },
    renge: { number: ren, ...renInfo },
    dige: { number: dige, ...diInfo },
    zongge: { number: zongge, ...zongInfo },
    wuxingCount,
    dominantWuxing,
    totalScore,
    assessment: totalScore >= 85 ? '大吉' : totalScore >= 70 ? '吉' : totalScore >= 50 ? '平' : '凶'
  }
}

